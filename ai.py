def detect_category(user_text: str):
    text = normalize_text(user_text)

    for category, words in KEYWORDS.items():
        for w in words:
            if normalize_text(w) in text:
                return category

    return None


def get_reply(category):
    global last_reply
    replies = REPLIES.get(category, ["เราอยู่ตรงนี้นะ 🤍"])

    available = [r for r in replies if r != last_reply]
    if not available:
        available = replies

    reply = random.choice(available)
    last_reply = reply
    return reply


def heal_reply(user_text: str):

    if not user_text.strip():
        return "พิมพ์มาได้เลยนะ 🤍"

    category = detect_category(user_text)

    if category == "suicide":
        return high_risk_reply()

    if category:
        return get_reply(category)

    return "เราอยู่ตรงนี้นะ เล่าเพิ่มได้นะ 🤍"